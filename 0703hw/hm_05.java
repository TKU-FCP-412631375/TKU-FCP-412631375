public class hm_05 {
    public static void main(String[] args) {
        int a = 12;
        int b = 7;
        int c = a + b;
        int d = a - b;
        int e = a * b;
        int f = a / b;
        int g = a % b;

        System.out.printf("和:       %d\n", c);
        System.out.printf("差:       %d\n", d);
        System.out.printf("積:       %d\n", e);
        System.out.printf("商:       %d\n", f);
        System.out.printf("餘數:     %d\n", g);
}
}